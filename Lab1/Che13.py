import turtle as t

t.pensize(3)
t.shape('turtle')
t.speed(0)
t.goto(0, -100)
t.color('black', 'yellow')
t.begin_fill()
t.circle(200)
t.end_fill()
t.penup()

t.goto(-70, 120)
t.color('red', 'blue')
t.pendown()
t.begin_fill()
t.circle(40)
t.end_fill()
t.penup()

t.goto(70, 120)
t.pendown()
t.begin_fill()
t.circle(40)
t.end_fill()
t.penup()

t.goto(-80, 40)
t.pensize(10)
t.color('red')
t.pendown()
t.right(90)
t.circle(80, 180)
t.penup()

t.color('black')
t.goto(0, 100)
t.setheading(0)
t.pendown()
t.right(110)
t.forward(60)
t.left(130)
t.forward(20)

t.hideturtle()
t.exitonclick()